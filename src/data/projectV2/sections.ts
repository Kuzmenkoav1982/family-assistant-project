export type ArchLayer = 'sources' | 'panorama' | 'reflection' | 'codes' | 'execution' | 'service';

export interface SectionV2 {
  id: string;
  label: string;           // текущее название
  labelNew?: string;       // новое название (если переименовываем)
  icon: string;
  hubId: string;           // текущий хаб-контейнер
  hubLabel: string;        // название хаба для режима "как есть"
  layer: ArchLayer;        // архитектурная роль
  path?: string;
  // карточка раздела
  tagline: string;
  purpose: string;
  sections: string[];      // что внутри
  dataFrom: string[];      // откуда берёт данные
  dataTo: string[];        // куда передаёт
  bridges: string[];       // мостики (текст кнопок перехода)
  notDoes: string[];       // что НЕ делает
  conflicts: string[];     // id конфликтов из OVERLAP_CASES
  changeNote?: string;     // что изменилось (для режима Сравнение)
}

export interface HubAsIs {
  id: string;
  label: string;
  icon: string;
  color: string;
  path?: string;
  problem?: string;        // в чём проблема текущей группировки
}

