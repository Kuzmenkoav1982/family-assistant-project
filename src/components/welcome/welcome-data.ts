export const trackSectionClick = async (sectionIndex: number, sectionTitle: string) => {
  try {
    const recentClicks = new Map<number, number>();
    const now = Date.now();
    const lastClick = recentClicks.get(sectionIndex);
    if (lastClick && now - lastClick < 10000) {
      return;
    }
    recentClicks.set(sectionIndex, now);

    const sessionId = localStorage.getItem('sessionId') || `session_${Date.now()}_${Math.random()}`;
    localStorage.setItem('sessionId', sessionId);
    
    await fetch('https://functions.poehali.dev/fe19c08e-4cc1-4aa8-a1af-b03678b7ba22', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        action: 'track_click',
        section_index: sectionIndex,
        section_title: sectionTitle,
        session_id: sessionId,
        user_agent: navigator.userAgent
      })
    });
  } catch (error) {
    console.debug('Analytics tracking failed:', error);
  }
};