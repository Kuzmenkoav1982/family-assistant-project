/**
 * Safety-перехват кризисных тем для ИИ-консультанта.
 *
 * Если в сообщении есть признаки суицида, самоповреждения, насилия или угрозы
 * ребёнку — мы НЕ отправляем запрос в LLM и показываем кризисный экран с
 * контактами экстренной помощи. ИИ не должен «консультировать» по таким темам.
 */

const CRISIS_PATTERNS: RegExp[] = [
  /суицид|суициид|покончить с собой|покончу с собой|не хочу жить|не хочется жить/i,
  /убить себя|убью себя|свести счёты с жизнью|свести счеты с жизнью|наложить на себя руки/i,
  /самоповреждени|режу себя|порезать себя|причинить себе вред|причиняю себе вред/i,
  /хочу умереть|лучше бы я умер|лучше умереть|нет смысла жить|смысла жить нет/i,
  /меня бьют|он меня бьёт|он меня бьет|насилие в семье|домашнее насилие|избива/i,
  /бью ребёнка|бью ребенка|угроза ребёнку|угроза ребенку|насилие над ребёнком|насилие над ребенком/i,
];

export function detectCrisis(text: string): boolean {
  if (!text) return false;
  return CRISIS_PATTERNS.some((re) => re.test(text));
}

